#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	l;

	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(fd, "-2147483648", 11);
			return ;
		}
		n = -n;
		write(fd, "-", 1);
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	l = n % 10 + 48;
	write(fd, &l, 1);
}
