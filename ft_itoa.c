#include "libft.h"

char	*ft_itoa(int n)
{
	char	a[12];
	int		i;
	int		s;

	i = 10;
	s = 0;
	a[11] = '\0';
	if (n == -2147483648)
		return (ft_strdup("-2147483648"));
	if (n < 0)
	{
		s = 1;
		n = -n;
	}
	if (n == 0)
		a[i--] = 48;
	while (n > 0)
	{
		a[i--] = (n % 10) + 48;
		n /= 10;
	}
	if (s)
		a[i--] = '-';
	return (ft_strdup(a + i + 1));
}
